        static void Main( )
        {
            WiBreastCancerDiag[ ] diagnoses = LoadWiBreastCancerDiagData( );
            
            double avgClass = 0;
            double clumpThickness = 0;
            double uniCellSize = 0;
            double uniCellShape = 0;
            double margAdhesion = 0;
            double epiCellSize = 0;
            double bareNuclei = 0;
            double blandChromatin = 0;
            double normalNucleoli = 0;
            double mitoses = 0;
            double counter = 0;
            
            // to iterate through to add all the values in the array 
            for ( int i = 0; i < diagnoses.Length; i++ )
            {
                avgClass += diagnoses[ i ].avgClass;
                clumpThickness += diagnoses[ i ].clumpThickness;
                uniCellSize += diagnoses[ i ].uniCellSize;
                uniCellShape += diagnoses[ i ].uniCellShape;
                margAdhesion += diagnoses[ i ].margAdhesion;
                epiCellSize += diagnoses[ i ].epiCellSize;
                bareNuclei += diagnoses[ i ].bareNuclei;
                blandChromatin += diagnoses[ i ].blandChromatin;
                normalNucleoli += diagnoses[ i ].normalNucleoli;
                mitoses += diagnoses[ i ].mitoses;
                counter = i;
            }
            
            // divides by counter to find the average 
            avgClass /= counter;
            clumpThickness /= counter;
            uniCellSize /= counter;
            uniCellShape /= counter;
            margAdhesion /= counter;
            epiCellSize /= counter;
            bareNuclei /= counter;
            blandChromatin /= counter;
            normalNucleoli /= counter;
            mitoses /= counter;
            
            // for rounding 
            avgClass *= 1000;
            avgClass = Math.Ceiling ( avgClass );
            clumpThickness *= 100;
            clumpThickness = Math.Floor ( clumpThickness );
            uniCellSize *= 100;
            uniCellSize = Math.Floor ( uniCellSize );
            epiCellSize *= 100;
            epiCellSize = Math.Floor ( epiCellSize );
            bareNuclei *= 100;
            bareNuclei = Math.Floor ( bareNuclei );
            mitoses *= 100;
            mitoses = Math.Floor ( mitoses );
            
            // displaying the results 
            string space = " ";
            WriteLine ( "Average Class {0,21}: {1:f3}", space, avgClass / 1000 );
            WriteLine ( "Average Clump Thickness {0,11}:  {1:f2}", space, clumpThickness / 100 );
            WriteLine ( "Average Uniformity of Cell Size {0,3}:  {1:f2}", space, uniCellSize / 100 );
            WriteLine ( "Average Uniformity of Cell Shape {0,2}:  {1:f2}", space, uniCellShape );
            WriteLine ( "Average Marginal Adhesion {0,9}:  {1:f2}", space, margAdhesion );
            WriteLine ( "Average Single Epithelial Cell Size:  {0:f2}", epiCellSize / 100); // longest string
            WriteLine ( "Average Bare Nuclei {0,15}:  {1:f2}", space, bareNuclei / 100 );
            WriteLine ( "Average Bland Chromatin {0,11}:  {1:f2}", space, blandChromatin );
            WriteLine ( "Average Normal Nucleoli {0,11}:  {1:f2}", space, normalNucleoli );
            WriteLine ( "Average Mitoses {0,19}:  {1:f2}", space, mitoses / 100 );
            
